  /* USER CODE BEGIN 2 */


  //  ------------ CONFIGURE PERIPHERALS TO RECEIVE INSTRUCTION --------------

  HAL_UART_Receive_IT(&huart1, &rxByte, 1);
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
